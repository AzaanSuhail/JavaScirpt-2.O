//Let us discuss primitive data type : primitive data types means single variable assinged to the variable or another definition can be that basic data types are called primitive data types

/*
1. number
2. string
3. boolean
4. undefined
5. null
*/

// 1. number

let num = 10; 
console.log(num);
console.log(typeof num); 

// 2. string

let str = "Hello World";
console.log(str);
console.log(typeof str);

// 3. boolean

let bool = true;
console.log(bool);
console.log(typeof bool);       

// 4. undefined

let und;
console.log(und);
console.log(typeof und);

// 5. null  

let nul = null;
console.log(nul);
console.log(typeof nul);

// 6. symbol   

let sym = Symbol('my symbol');
console.log(sym);
console.log(typeof sym);

// 7. bigint 

let bigInt = 1000000000000000000000000000000000000000000000000000n;
console.log(bigInt);
console.log(typeof bigInt);

// 8. object // it is non-primitive data where more than value value assigned to a single variable called object.

let obj = {name: "John", age: 30};
console.log(obj);
console.log(typeof obj);

let arr = [1, 2, 3, 4, 5];
console.log(arr);
console.log(typeof arr);

/*
Small trick to learn all these datatypes : NN SS BB U

N - null
N - number
S - symbol
S - string
B - boolean
B - bigint
U - undefine
*/

